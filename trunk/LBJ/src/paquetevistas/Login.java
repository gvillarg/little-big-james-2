    private void btnOlvidoContrasenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOlvidoContrasenaActionPerformed
        // TODO add your handling code here:
        usuarioContrasena = mycontroladorseguridad.getUsuarioByNombre(txtUsuario.getText());
        if (usuarioContrasena == null) {
            JOptionPane.showMessageDialog(null, "Usuario no encontrado. Ingrese de nuevo el nombre de usuario.");
        } else {
            int resp = JOptionPane.showConfirmDialog(null, "Desea el envío de la nueva contraseña a su correo?", "Restablecer Contraseña", JOptionPane.INFORMATION_MESSAGE);
            if (resp == 0) {
                String nuevopass = mycontroladorseguridad.generarNuevaContrasena(8);
                mycontroladorseguridad.editarContrasena(usuarioContrasena, "", nuevopass);
                JavaMailDAO correo = new JavaMailDAO(usuarioContrasena);
                JOptionPane.showMessageDialog(null, "Se envió la nueva contraseña a su correo.");
            }
        }
    }//GEN-LAST:event_btnOlvidoContrasenaActionPerformed

