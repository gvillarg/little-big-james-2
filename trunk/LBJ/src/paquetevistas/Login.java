    private void iniciarSesion() {
        try {
            setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));

            if (txtPassword.getText().isEmpty() && txtUsuario.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Debe ingresar los campos de usuario y contraseña.");
            } else if (txtUsuario.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Debe ingresar el campo de usuario.");
            } else if (txtPassword.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Debe ingresar el campo de contraseña.");
            } else {
                String pass = new String(txtPassword.getPassword());
                usuario = mycontroladorseguridad.login2(txtUsuario.getText(), pass);

                if (usuario == null) {
                    JOptionPane.showMessageDialog(null, "Usuario y/o Contraseña incorrecta. Ingrese de nuevo los datos.");
                    txtUsuario.setText("");
                    txtPassword.setText("");
                } else {
                    escritorio = new Escritorio();
                    LBJ.Login.setVisible(false);
                    escritorio.setVisible(true);
                }
            }
        } finally {
            setCursor(Cursor.getDefaultCursor());
        }
    }

