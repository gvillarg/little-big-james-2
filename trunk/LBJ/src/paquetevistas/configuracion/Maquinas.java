    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        // Cargar Tabla
        if (evt.getClickCount() == 2) {
            int row = jTable3.getSelectedRow();
            String nom = jTable3.getValueAt(row, 0).toString();
            maquinaaeditar = mycontroladorconfiguracion.sacamaquinaxnombre(nom);
            maquinaEditar = new MaquinaEditar();
            Escritorio.maquina.getDesktopPane().add(maquinaEditar);
            maquinaEditar.setVisible(true);
        }
    }//GEN-LAST:event_jTable3MouseClicked

