    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Para eliminar maquinas
        if (JOptionPane.showConfirmDialog(rootPane, "Â¿Desea realmente eliminar esta maquina?",
                "Mensaje", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
              int row = jTable3.getSelectedRow();
              String nombre = jTable3.getValueAt(row, 0).toString();
              modelos.Maquina maq = mycontroladorconfiguracion.sacamaquinaxnombre(nombre);
              mycontroladorconfiguracion.eliminarmaquina(maq);
              refreshMaquinasTable();
              
        }
    }//GEN-LAST:event_jButton3ActionPerformed

