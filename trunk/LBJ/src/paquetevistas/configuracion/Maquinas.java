    class MaquinaTableModel extends AbstractTableModel{
    
        List<modelos.Maquina> maq = new ArrayList<modelos.Maquina>();
        
        public MaquinaTableModel() {
            maq = mycontroladorconfiguracion.sacamaquinas();
        }
        
        String[] titles = {"Nombre", "Estado"};

        @Override
        public int getRowCount() {
            return maq.size();
        }

        @Override
        public int getColumnCount() {
            return 2;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            String res=null;
            int estado;
            modelos.Maquina m = maq.get(rowIndex);
            switch (columnIndex){
                case 0:
                    res= m.getNombre();
                    break;
                case 1:
                    estado= m.getEstado();
                    if (estado==1)
                          res="Activo";
                                 else res="Inactivo";
                    break;
                    
            }
            return res;
        }
        
        public String getColumnName(int col) {
            return titles[col];
        }
    
    }
    
