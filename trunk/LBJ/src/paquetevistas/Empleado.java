    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        //Para editar
        if (evt.getClickCount() == 2) {
            int row = jTable1.getSelectedRow();
            String ids = jTable1.getValueAt(row, 0).toString();
            int id = Integer.parseInt(ids);
            empleadoaeditar = mycontroladorconfiguracion.sacaempleadoxid(id);
            empleadoEditar = new EmpleadoEditar();
            Escritorio.empleado.getDesktopPane().add(empleadoEditar);
            empleadoEditar.setVisible(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
            int row = jTable1.getSelectedRow();
            int id = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
            Usuario us = mycontroladorconfiguracion.sacaUsuarioxEmp(id);
            if(us != null){
                if (JOptionPane.showConfirmDialog(rootPane, " El empleado que desea eliminar tiene un Usuario ¿Desea realmente eliminar este empleado y su usuario?",
                "Mensaje", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    modelos.Empleado e = mycontroladorconfiguracion.sacaempleadoxid(id);
                    mycontroladorconfiguracion.eliminarempleado(e);
                    mycontroladorseguridad.eliminarUsuario2(us);
                    refreshEmpleadoTable();            
                }
            }
            else{
                if (JOptionPane.showConfirmDialog(rootPane, "¿Desea realmente eliminar este empleado?",
                "Mensaje", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                modelos.Empleado e = mycontroladorconfiguracion.sacaempleadoxid(id);
                mycontroladorconfiguracion.eliminarempleado(e);
                refreshEmpleadoTable();
                //TextilMain.myLoginFrame.myMainFrame.myMPIFrame.refreshProductTable();
                mycontroladorseguridad.insertarlog(usuario, new Date(), "Eliminar", "Empleado", "Eliminar Empleado");
        }
      }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        char car = evt.getKeyChar();
        if((car<'a' || car>'z') && (car<'A' || car>'Z') && (car!=(char) KeyEvent.VK_BACK_SPACE)) {
            evt.consume();
            JOptionPane.showMessageDialog(this, "Debe insertar solo letras en el campo de Busqueda");
       
        }
    }//GEN-LAST:event_jTextField1KeyTyped

