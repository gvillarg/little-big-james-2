    class EmpleadoTableModel extends AbstractTableModel{

        List<modelos.Empleado> emp = new ArrayList<modelos.Empleado>();
        
        public EmpleadoTableModel(){
            emp = mycontroladorconfiguracion.sacaempleados();
        }
        String[] titles = {"Codigo", "Nombre", "Apellido Paterno", "Estado"};
        
        @Override
        public int getRowCount() {
            return emp.size();
        }

        @Override
        public int getColumnCount() {
            return 4;}

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            String res = null;
            int tipo;
            int estado;
            String nombre = null;
            modelos.Empleado e = emp.get(rowIndex);
            switch (columnIndex) {
                case 0:
                    res = "" + e.getIdempleado();
                    break;
                case 1:
                    res = e.getNombre();
                    break;
                case 2:
                    res=e.getAppaterno();
                    break;
                case 3:
                    estado=e.getEstado();
                    if (estado==1)
                          res="Activo";
                                 else res="Cesado";
                    break;
            }
            
            return res;
        }
        
        public String getColumnName(int col) {
            return titles[col];
        }
    
    }
    
