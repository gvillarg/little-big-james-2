    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Long ruc;
        String razonSocial=jrazonSocial.getText();
        if((jruc.getText().isEmpty())){
            ruc=0L;
        }else if(!validacion.es_numero_entero(jruc.getText())){
                ruc=-1L;
            }else{
          ruc= Long.parseLong(jruc.getText());  
          Long limitinf=9999999999L;
          Long limitsup=100000000000L;
          if((ruc.compareTo(limitinf)<0)||(ruc.compareTo(limitsup)>0)){
              ruc=-1L;
          }
        }       
//        System.out.println(jComboBox2.getSelectedItem().toString());
        String producto=jComboBox2.getSelectedItem().toString();
        String estado=jComboBox1.getSelectedItem().toString();
        mySupplierTableModel.prov = mycontroladorcompra.buscaproveedores(razonSocial, ruc, producto, estado);
        mySupplierTableModel.fireTableChanged(null);
        
        mycontroladorseguridad.insertarlog(usuario, new Date(), "Consultar", "Proveedor", "Consultar Proveedor");
    }//GEN-LAST:event_jButton2ActionPerformed

