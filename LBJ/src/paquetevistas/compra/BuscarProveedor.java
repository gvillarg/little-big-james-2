    class SupplierTableModel extends AbstractTableModel {
        
        List<Proveedor> prov = new ArrayList<Proveedor>();
        
        public SupplierTableModel() {
            prov = mycontroladorcompra.sacaproveedores();
        }
        
        String[] titles = {"Código", "Razón Social", "Teléfono", "Correo"};
        
        public int getRowCount() {
            return prov.size();
        }

        public int getColumnCount() {
            return 4;
        }

        public Object getValueAt(int rowIndex, int columnIndex) {
            String res = null;
            Proveedor p = prov.get(rowIndex);
            
            switch(columnIndex){
                case 0:
                    res=""+p.getIdproveedor();
                    break;
                case 1:
                    res=""+p.getRazonsocial();
                    break;
                case 2: 
                    res=""+p.getTelefono();
                    break;
                case 3:
                    res=""+p.getCorreo();
                   break;
            }
            return res;
        }
        
         public String getColumnName(int col) {
            return titles[col];
        }
        
    }
