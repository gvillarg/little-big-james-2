    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // SI NO HAY RUTA
        if ("".equals(jNombreArchivo.getText())) {
            JOptionPane.showMessageDialog(this, "Ruta de archivo no especificada.");
            return;
        }
        
        //AQUI EMPIEZO A LEER
        
        if (JOptionPane.showConfirmDialog(this,
                "¿Está seguro de agregar las Maquinas desde " + jNombreArchivo.getText() + "?", "Carga Masiva de Maquinas", JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION){
            //Empiezo a reccorre
            int fila = 0;
            int col = 0;
            
            try{
                Workbook archivoExcel = Workbook.getWorkbook(new File(jNombreArchivo.getText()));
                
                Sheet hoja = archivoExcel.getSheet(0);
                int numColumnas = hoja.getColumns();
                int numFilas = hoja.getRows();
                
                
                List<modelos.Maquina> maquinas = new ArrayList<modelos.Maquina>();
                List<modelos.Maquina> maquinasexistentes = new ArrayList<modelos.Maquina>();
                int existencia;
                int estadoid;
                for (fila = 1; fila < numFilas; fila++) {
                    existencia=0;
                    col=0;
                    
                    modelos.Maquina maquina = new modelos.Maquina();
                    String descripcion, nombre,actividad,estado;
                    
                    nombre= hoja.getCell(col++, fila).getContents();
                    descripcion = hoja.getCell(col++, fila).getContents();
                    actividad= hoja.getCell(col++, fila).getContents();
                    estado = hoja.getCell(col++, fila).getContents();
                
                    if(estado.equalsIgnoreCase("Activo")){estadoid=1;}
                        else estadoid=0;
                    
                    modelos.Actividad act = mycontroladoralmacen.sacaactividadbyname(actividad);
                    
                    maquina.setNombre(nombre);
                    maquina.setDescripcion(descripcion);
                    maquina.setEstado(estadoid);
                    maquina.setActividad(act);
                    
                    existencia = mycontroladorconfiguracion.verificaexistenciamaquina(nombre);
                    if (existencia == 1) {
                        maquinasexistentes.add(maquina);
                    } else {
                        maquinas.add(maquina);
                    }
                    
                 }
                    
                    //AGREGO SEGUN CONDICIONES
                    if(maquinas.size() > 0 && maquinasexistentes.size()==0){
                        mycontroladorconfiguracion.guardaListamaquina(maquinas);
                        JOptionPane.showMessageDialog(this, "Se agregaron las maquinas exitosamente.");
                        lbj.LBJ.Login.escritorio.maquina.refreshMaquinasTable();
                        this.dispose();
                    } else if (maquinas.size() == 0 && maquinasexistentes.size() > 0) {
                        JOptionPane.showMessageDialog(this, "Los maquinas que intenta guardar ya existen.");
                        this.dispose();
                    } else if(maquinas.size() > 0 && maquinasexistentes.size()>0){
                        mycontroladorconfiguracion.guardaListamaquina(maquinas);
                        JOptionPane.showMessageDialog(this, "Se agregaron las maquinas nuevas exitosamente. Algunas ya existian");
                        lbj.LBJ.Login.escritorio.maquina.refreshMaquinasTable();
                        this.dispose();
                    } else if (maquinas.size() == 0 && maquinasexistentes.size() > 0) {
                    JOptionPane.showMessageDialog(this, "El archivo que intenta cargar esta vacio.");
                    this.dispose();
                    }
                    
                    //Insertar Log
                    mycontroladorseguridad.insertarlog(usuario, new Date(), "Insertar", "Maquina", "Registrar Maquina");
               
                
            }
            catch(Exception ioe){
                ioe.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error: En el Archivo, en la Fila " + (fila + 1) + " y Columna " + col);
            }
            
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

