    class OrdenCompraTableModel extends AbstractTableModel{

        public OrdenCompraTableModel(){
            //FALTA BUSCAR ORDEN
            
            mycontroladoralmacen.mySort(orden2);
            mycontroladoralmacen.acumulate(orden2);
            
        

        }
        
        String[] titles = {"Nombre", "Cantidad", "Unidad"};
        
        @Override
        public int getRowCount() {
             return orden2.size();
        }

        @Override
        public int getColumnCount() {
            return 3;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            String res=null;
            modelos.Productoxreceta p = orden2.get(rowIndex);
            switch (columnIndex){
                case 0: res = "" + p.getProducto().getNombre();
                        break;
                case 1: 
                        if(p.getCantidad()< p.getProducto().getStock())
                        res = "" + 0;
                        else res = "" + (p.getCantidad()-p.getProducto().getStock());
                        break;
                case 2: res = "" + p.getProducto().getUnidadmedida().getNombre();
                        break; 
            }
            return res;
        }
        
        public String getColumnName(int col){
            return titles[col];
        } 

}

